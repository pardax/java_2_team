
public class PrizeTable {
	private String n;
	private String lNum;
	private String wt;
	
	public PrizeTable(String n, String lNum, String wt) {
		this.n = n;
		this.lNum = lNum;
		this.wt = wt;

	}
	
	public String getN() {
		return n;
	}
	
	public String getlNum() {
		return lNum;
	}
	
	public String getWt() {
		return wt;
	}
	
}
